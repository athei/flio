#[cfg(test)]
mod tests {
    use super::*;
    use strum::IntoEnumIterator;

    impl NTStatus {
        fn is_custom(self) -> bool {
            (((self as u32) >> 28) & 0x1) == 1
        }

        fn reserved_field(self) -> u32 {
            ((self as u32) >> 29) & 0x1
        }
    }

    #[test]
    fn no_custom() {
        for status in NTStatus::iter() {
            assert!(!status.is_custom());
        }
    }

    #[test]
    fn all_reserved() {
        for status in NTStatus::iter() {
            assert!(status.reserved_field() == 0);
        }
    }

    #[test]
    fn all_severity_exit() {
        for status in NTStatus::iter() {
            status.facility();
        }
    }
}
